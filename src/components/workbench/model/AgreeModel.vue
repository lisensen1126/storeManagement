        h3 第三条 费用以及付款
        p （1）畅移与门店双方以人民币为结算货币。
        p （2）平台默认以门店申请美团支付时提交的账户名称以及账号。
        p （3）“壳保养智慧门店”平台根据双方确认的商品结算价格向门店结算货款。
        p （4）门店使用平台服务，门店同意按照平台标准支付手续费（以微信支付行业费率为准，以下简称“手续费”）。平台将从提供支付服务的款项中实时扣除手续费，手续费的费用为: 0.6%，随微信支付的政策调整有变化的可能，平台暂免收取平台手续费。产品协议对费用及结算另有约定的，以产品协议约定为准。
        p （5）门店需及时向畅移提供平台方要求提供的变更账户相关资料。因门店银行账号变更但未及时通知畅移造成的任何损失均由门店承担。
        p （6）平台、门店双方约定的结算周期以双方签订的服务协议约定30天为准。每月最后一个工作日为结算日，平台、门店双方应进行对账，平台结算当月内已核销的商品或服务货款给门店。具体结算到达门店银行账号的时间取决 于银行系统的清算时长。

