        p 13、门店有义务按照消费者实际支付的金额为消费者开具发票，相关税收应按国家相关规定由门店自行承担，平台不承担开票义务。
        p 14、未核销的订单，消费者可在30天内申请退款，由畅移原路退款给消费者，超过30天后由门店负责消费者的退款，门店可按消费者实际付款金额扣除0.6%的手续费退款给消费者，平台当月底按正常核销订单与门店进行清算。如果已核销订单，消费者有退款等售后问题，由门店承担。
        h3 第六条 保密义务
        p 1、一方对于本协议的签订、内容及在履行本协议期间所获知的其他方的商业秘密负有保密义务。非经相对方书面同意，不得向相对方以外的任何第三方（关联公司除外）泄露、给予或转让该等保密信息。根据法律法规、证券交易所规则向政府、证券交易所和/或其他监管机构提供、协议方的法律、会计、商业及其他顾问、雇员除外）。
        p 2、如相对方提出要求，任何一方均应将载有相对方单独所有的保密信息的任何文件、资料或软件等，在本协议终止后按对方要求归还对方，或予以销毁，或进行其他处置，并且不得继续使用这些保密信息。
        p 3、在本协议终止之后，各方在本条款项下的义务并不随之终止，各方仍需遵守本协议之保密条款，履行其所承诺的保密义务，直到其他方同意其解除此项义务，或事实上不会因违反本协议的保密条款而给其他方造成任何形式的损害时为止。
        p 4、任何一方均应告知并督促其因履行本协议之目的而必须获知本协议内容及因合作而获知对方商业秘密的雇员、代理人、顾问等遵守保密条款，并对其雇员、代理人、顾问等的行为承担责任。
        h3 第七条 违约责任
