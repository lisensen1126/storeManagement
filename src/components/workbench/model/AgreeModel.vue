        p （1）门店应依据平台规则及相应的入驻流程要求完成“壳保养智慧门店”平台的入驻及店铺开设，在线签订“壳保养智慧门店”平台所公示的需要门店签订的相应协议。门店应向平台提交各项为经营店铺所必须的资质、证照、证明或其他相关文件（以下统称“证明文件”），包括但不限于三证合一（营业执照、税务登记证、组织机构代码）、开户行证明、法定代表人身份证正、反面复印件等，门店应向“壳保养智慧门店”平台提交与原件核对一致且加盖门店公章的纸质复印件（根据实际业务提供相应资质）。
        p （2）门店保证上述证明文件发生任何变更或更新时立即通知平台方，并于变更或更新之日起十五个工作日内，提交更新后的文件并依据“壳保养智慧门店”平台实时公布的流程进行相应认证。
