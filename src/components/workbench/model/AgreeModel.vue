        p （3）门店提交的任何信息均真实、合法、有效，所使用的图片、文字等不侵犯任何第三方合法权益；
        p （4）门店签署本协议并同意“壳保养智慧门店”平台规则内容；
        p （5）平台依据国家法律法规、政策或其他规范性文件规定及经营需要可能设定的其他条件。
