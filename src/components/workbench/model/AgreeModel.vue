        p 2、畅移有权根据“壳保养智慧门店”平台的发展规划自主选择是否对通过资质审核的门店开设店铺，提供平台服务。而未承诺必然对所有申请的门店承诺开通权限；同时有权依据独立判断，不受时间限制的审批门店的申请。
        p 3、畅移有权单方根据国家相关法律法规、政策及“壳保养智慧门店”平台运营情况，对公示于“壳保养智慧门店”平台规则进行变更，变更后的规则平台将以公告形式告知门店，任何变更一经公告即构成本协议的组成部分。
        p 4、畅移有权对门店的申请信息、上传的相关数据信息、在“壳保养智慧门店”平台发布的其他信息、交易行为进行监督检查，对发现的涉嫌违反法律法规、违反平台规则或的信息及其相关内容，门店知晓并同意畅移有权不经通知直接删除，对发现的其他问题或疑问门店发出询问及要求改正的通知，门店应在接到通知后立即做出说明并提供相关证明材料或改正。
